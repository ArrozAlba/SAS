	function valida(){
	if(document.form_medicamento.nombre.value==''){
		document.form_medicamento.nombre.focus();
		jAlert('El campo \"Nombre\" no puede estar vacio!','Dialogo de Alerta');
	return false;
	}
	if(document.form_medicamento.nombre.value.length <3 ) {		
		jAlert('El campo \"Nombre" no puede ser menor a 3 caracteres!','Dialogo de Alerta');
		document.form_medicamento.nombre.focus();
	return false;
	}
		if(document.form_medicamento.presentacion.value==''){
		document.form_medicamento.presentacion.focus();
		jAlert('El campo \"Prsentación\" no puede estar vacio!','Dialogo de Alerta');
	return false;
	}
	if(document.form_medicamento.presentacion.value.length <3 ) {		
		jAlert('El campo \"Prsentación" no puede ser menor a 3 caracteres!','Dialogo de Alerta');
		document.form_medicamento.presentacion.focus();
	return false;
	}
		if(document.form_medicamento.componente.value==''){
		document.form_medicamento.componente.focus();
		jAlert('El campo \"Componente\" no puede estar vacio!','Dialogo de Alerta');
	return false;
	}
	if(document.form_medicamento.componente.value.length <3 ) {		
		jAlert('El campo \"Componente" no puede ser menor a 3 caracteres!','Dialogo de Alerta');
		document.form_medicamento.componente.focus();
	return false;
	}
return true;
}	